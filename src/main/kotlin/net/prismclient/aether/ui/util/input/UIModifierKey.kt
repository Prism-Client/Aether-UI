package net.prismclient.aether.ui.util.input

/**
 * [UIModifierKey] are keys which cannot be converted into characters. They are keys
 * such as Shift, Alt, Ctrl etc...
 *
 * @author sen
 * @since 6/15/2022
 */
enum class UIModifierKey {
    LEFT_CTRL,
    RIGHT_CTRL,
    LEFT_SHIFT,
    RIGHT_SHIFT,
    LEFT_ALT,
    RIGHT_ALT,
    ARROW_LEFT,
    ARROW_RIGHT,
    ARROW_UP,
    ARROW_DOWN,
    TAB,
    ESCAPE,
    CAPS_LOCK,
    BACKSPACE,
    ENTER,
}